            String m = empid.getText();
           con = DriverManager.getConnection("jdbc:mysql://localhost:3306/empdb_final","root","");   
        
         JasperDesign jd = JRXmlLoader.load("C:\\Users\\BADHAN\\Documents\\NetBeansProjects\\EmployeeMangementSystem\\src\\report\\EMPInfoReport.jrxml");
         String sql ="SELECT * FROM recruit WHERE emp_id='"+ m +"' ";
         JRDesignQuery newQuery = new JRDesignQuery();
